using System;
using System.Collections.Generic;
using System.IO;
using System.Web.UI;

[Serializable]
public class GridSettingsCollection
{
	private int _pageSize;

	private object[] _groupByExpressionsStates;

	private object _sortExpressionsState;

	private string _filterExpression;

	private List<ColumnSettings> _columnSettings;

	private List<ColumnSettings> _autoColumnSettings;

	public List<ColumnSettings> AutoGeneratedColumnSettings
	{
		get
		{
			return this._autoColumnSettings;
		}
		set
		{
			this._autoColumnSettings = value;
		}
	}

	public List<ColumnSettings> ColumnSettings
	{
		get
		{
			return this._columnSettings;
		}
		set
		{
			this._columnSettings = value;
		}
	}

	public string FilterExpression
	{
		get
		{
			return this._filterExpression;
		}
		set
		{
			this._filterExpression = value;
		}
	}

	public object[] GroupByExpressionsStates
	{
		get
		{
			return this._groupByExpressionsStates;
		}
		set
		{
			this._groupByExpressionsStates = value;
		}
	}

	public int PageSize
	{
		get
		{
			return this._pageSize;
		}
		set
		{
			this._pageSize = value;
		}
	}

	public object SortExpressionsState
	{
		get
		{
			return this._sortExpressionsState;
		}
		set
		{
			this._sortExpressionsState = value;
		}
	}

	public GridSettingsCollection()
	{
	}

	public static GridSettingsCollection LoadFromSerializedData(string data)
	{
		return (GridSettingsCollection)(new LosFormatter()).Deserialize(data);
	}

	public static GridSettingsCollection LoadFromSerializedData(byte[] data)
	{
		return (GridSettingsCollection)(new LosFormatter()).Deserialize(new MemoryStream(data));
	}

	public byte[] ToArray()
	{
		LosFormatter losFormatter = new LosFormatter();
		MemoryStream memoryStream = new MemoryStream();
		losFormatter.Serialize(memoryStream, this);
		return memoryStream.ToArray();
	}

	public override string ToString()
	{
		LosFormatter losFormatter = new LosFormatter();
		StringWriter stringWriter = new StringWriter();
		losFormatter.Serialize(stringWriter, this);
		return stringWriter.ToString();
	}
}