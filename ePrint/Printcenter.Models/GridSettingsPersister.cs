using System;
using System.Collections;
using System.Collections.Generic;
using System.Runtime.CompilerServices;
using System.Web.UI;
using System.Web.UI.WebControls;
using Telerik.Web.UI;

public class GridSettingsPersister
{
	private RadGrid _grid;

	private GridSettingsType _persistedSettingTypes;

	private GridSettingsCollection _settings;

	public RadGrid Grid
	{
		get
		{
			return this._grid;
		}
	}

	public virtual GridSettingsType PersistedSettingTypes
	{
		get
		{
			return this._persistedSettingTypes;
		}
		set
		{
			this._persistedSettingTypes = value;
		}
	}

	protected virtual GridSettingsCollection Settings
	{
		get
		{
			return this._settings;
		}
		set
		{
			this._settings = value;
		}
	}

	public GridSettingsPersister(RadGrid grid) : this(grid, GridSettingsType.All)
	{
	}

	public GridSettingsPersister(RadGrid grid, GridSettingsType persistedSettingFlags)
	{
		this._grid = grid;
		this._persistedSettingTypes = persistedSettingFlags;
		this._settings = new GridSettingsCollection()
		{
			ColumnSettings = new List<ColumnSettings>(),
			AutoGeneratedColumnSettings = new List<ColumnSettings>()
		};
	}

	private ColumnSettings GetColumnSettings(GridColumn column)
	{
		ColumnSettings columnSetting = new ColumnSettings()
		{
			UniqueName = column.UniqueName,
			Width = column.HeaderStyle.Width,
			Visible = column.Visible,
			Display = column.Display,
			OrderIndex = column.OrderIndex,
			CurrentFilterFunction = column.CurrentFilterFunction,
			CurrentFilterValue = column.CurrentFilterValue
		};
		return columnSetting;
	}

	public virtual GridSettingsCollection GetSavedSettings()
	{
		if (this.Grid == null)
		{
			throw new NullReferenceException();
		}
		if (this.IsSettingSpecified(GridSettingsType.Paging))
		{
			this.SavePagingSettings();
		}
		if (this.IsSettingSpecified(GridSettingsType.Grouping))
		{
			this.SaveGroupByExpressions();
		}
		if (this.IsSettingSpecified(GridSettingsType.Sorting))
		{
			this.SaveSortExpressions();
		}
		if (this.IsSettingSpecified(GridSettingsType.Filtering))
		{
			this.SaveFilterExpression();
		}
		if (this.IsSettingSpecified(GridSettingsType.ColumnSettings))
		{
			this.SaveColumnSettings();
		}
		return this.Settings;
	}

	private void Grid_ColumnCreated(object sender, GridColumnCreatedEventArgs e)
	{
		ColumnSettings columnSetting = this.Settings.AutoGeneratedColumnSettings.Find((ColumnSettings set) => set.UniqueName == e.Column.UniqueName);
		GridColumn column = e.Column;
		if (columnSetting != null)
		{
			this.SetColumnSettings(ref column, columnSetting);
		}
	}

	protected bool IsSettingSpecified(GridSettingsType settingType)
	{
		if ((this.PersistedSettingTypes & GridSettingsType.All) == GridSettingsType.All)
		{
			return true;
		}
		return (this.PersistedSettingTypes & settingType) == settingType;
	}

	protected virtual void LoadColumnSettings()
	{
		if (this.Settings.AutoGeneratedColumnSettings.Count > 0)
		{
			this.Grid.ColumnCreated += new GridColumnCreatedEventHandler(this.Grid_ColumnCreated);
		}
		foreach (ColumnSettings columnSetting in this.Settings.ColumnSettings)
		{
			GridColumn columnSafe = this.Grid.MasterTableView.GetColumnSafe(columnSetting.UniqueName);
			if (columnSafe == null)
			{
				continue;
			}
			this.SetColumnSettings(ref columnSafe, columnSetting);
		}
	}

	protected virtual void LoadFilterExpression()
	{
		this.Grid.MasterTableView.FilterExpression = this.Settings.FilterExpression;
	}

	protected virtual void LoadGroupByExpressions()
	{
		if (this.Settings.GroupByExpressionsStates == null)
		{
			return;
		}
		this.Grid.MasterTableView.GroupByExpressions.Clear();
		object[] groupByExpressionsStates = this.Settings.GroupByExpressionsStates;
		for (int i = 0; i < (int)groupByExpressionsStates.Length; i++)
		{
			object obj = groupByExpressionsStates[i];
			GridGroupByExpression gridGroupByExpression = new GridGroupByExpression();
			((IStateManager)gridGroupByExpression).LoadViewState(obj);
			this.Grid.MasterTableView.GroupByExpressions.Add(gridGroupByExpression);
		}
	}

	protected virtual void LoadPagingSettings()
	{
		if (this.Grid.MasterTableView.AllowPaging && this.Settings.PageSize > 0)
		{
			this.Grid.MasterTableView.PageSize = this.Settings.PageSize;
		}
	}

	public virtual void LoadSettings(string value)
	{
		this.LoadSettings(GridSettingsCollection.LoadFromSerializedData(value));
	}

	public virtual void LoadSettings(byte[] data)
	{
		this.LoadSettings(GridSettingsCollection.LoadFromSerializedData(data));
	}

	public virtual void LoadSettings(GridSettingsCollection settings)
	{
		if (this.Grid == null || settings == null)
		{
			throw new NullReferenceException();
		}
		this.Settings = settings;
		if (this.IsSettingSpecified(GridSettingsType.Paging))
		{
			this.LoadPagingSettings();
		}
		if (this.IsSettingSpecified(GridSettingsType.Grouping))
		{
			this.LoadGroupByExpressions();
		}
		if (this.IsSettingSpecified(GridSettingsType.Sorting))
		{
			this.LoadSortExpressions();
		}
		if (this.IsSettingSpecified(GridSettingsType.Filtering))
		{
			this.LoadFilterExpression();
		}
		if (this.IsSettingSpecified(GridSettingsType.ColumnSettings))
		{
			this.LoadColumnSettings();
		}
	}

	protected virtual void LoadSortExpressions()
	{
		if (this.Settings.SortExpressionsState == null)
		{
			return;
		}
		((IStateManager)this.Grid.MasterTableView.SortExpressions).LoadViewState(this.Settings.SortExpressionsState);
	}

	protected virtual void SaveColumnSettings()
	{
		this.Settings.ColumnSettings.Clear();
		foreach (GridColumn column in this.Grid.MasterTableView.Columns)
		{
			this.Settings.ColumnSettings.Add(this.GetColumnSettings(column));
		}
		this.Settings.AutoGeneratedColumnSettings.Clear();
		GridColumn[] autoGeneratedColumns = this.Grid.MasterTableView.AutoGeneratedColumns;
		for (int i = 0; i < (int)autoGeneratedColumns.Length; i++)
		{
			GridColumn gridColumn = autoGeneratedColumns[i];
			this.Settings.AutoGeneratedColumnSettings.Add(this.GetColumnSettings(gridColumn));
		}
	}

	protected virtual void SaveFilterExpression()
	{
		this.Settings.FilterExpression = this.Grid.MasterTableView.FilterExpression;
	}

	protected virtual void SaveGroupByExpressions()
	{
		this.Settings.GroupByExpressionsStates = new object[this.Grid.MasterTableView.GroupByExpressions.Count];
		for (int i = 0; i < (int)this.Settings.GroupByExpressionsStates.Length; i++)
		{
			this.Settings.GroupByExpressionsStates[i] = ((IStateManager)this.Grid.MasterTableView.GroupByExpressions[i]).SaveViewState();
		}
	}

	protected virtual void SavePagingSettings()
	{
		this.Settings.PageSize = this.Grid.MasterTableView.PageSize;
	}

	public virtual string SaveSettings()
	{
		return this.GetSavedSettings().ToString();
	}

	protected virtual void SaveSortExpressions()
	{
		this.Settings.SortExpressionsState = ((IStateManager)this.Grid.MasterTableView.SortExpressions).SaveViewState();
	}

	private void SetColumnSettings(ref GridColumn column, ColumnSettings setting)
	{
		column.Display = setting.Display;
		column.Visible = setting.Visible;
		column.HeaderStyle.Width = setting.Width;
		column.OrderIndex = setting.OrderIndex;
		column.CurrentFilterFunction = setting.CurrentFilterFunction;
		column.CurrentFilterValue = setting.CurrentFilterValue;
	}
}