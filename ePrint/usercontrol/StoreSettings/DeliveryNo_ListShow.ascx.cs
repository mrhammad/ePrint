        public void GridBind_Customer(long EstimateID)
        {
            DataTable dataTable = SettingsBasePage.DeliveryNote_Number_View(this.CompanyID, EstimateID);
            foreach (DataRow row in dataTable.Rows)
            {
                row["DeliveryNumber"] = this.objBase.SpecialDecode(row["DeliveryNumber"].ToString());
                DeliveryID = row["DeliveryID"].ToString();
            }
            this.RadGrid_Customer.DataSource = dataTable;
            if (dataTable.Rows.Count > 0)
            {
                this.RadGrid_Customer.DataBind();
            }
        }
